#  Strings
"""
str - strings
É tudo que está dentro de aspas
"""

print('Alguma coisa')
print("Aspas duplas")
print(123456)

print('Essa é uma "string" (str).')
print("Essa é uma 'string' (str).")
print('Esse é meu \"texto\" (str).')  #  Usando caractére de escape
print("Esse é meu \'texto\' (str).")  #  Usando caractére de escape
print("Esse é meu \n 'texto\' (str).")  #  Usando caractére de escape
print(r"Esse é meu \n 'texto\' (str).")  #  Usando caractére para não executar o que está dentro do código
print('Esse é meu "aspas aqui" (str).')


